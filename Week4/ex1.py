# string functions
# a string is a series of characters
output = chr(65)
print(output)
output = ord('A')
print(output)

# repeat strings
print("Hello there: " * 2)
print("You have earned", "*" * 5, "stars!")

# strings as a collection of characters
str = "Hello"
print(len(str))
print(str[0])
print(str[-1])

print(str[4])
